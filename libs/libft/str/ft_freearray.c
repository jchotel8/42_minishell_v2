#include "../libft.h"

void	ft_freearray(char **arr)
{
	int i;

	i = 0;
	while (arr && arr[i] && arr[i] != NULL)
		free(arr[i++]);
	if (arr != NULL)
		free(arr);
	arr = NULL;
}
